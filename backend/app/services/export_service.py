    @staticmethod
    def to_docx(mom: Dict) -> io.BytesIO:
        """Generates a downloadable DOCX binary file stream."""
        doc = DocxDocument()
        doc.add_heading(mom.get("title", "Minutes of Meeting"), level=0)
        doc.add_paragraph(f"Date: {mom.get('date')} | Generated by InsightOrion")

        # Executive Summary
        doc.add_heading("Executive Summary", level=1)
        doc.add_paragraph(mom.get("executive_summary", ""))

        # Agenda
        doc.add_heading("Agenda Covered", level=1)
        for agenda in mom.get("agenda_covered", []):
            doc.add_paragraph(agenda, style="List Bullet")

        # Discussion
        doc.add_heading("Key Discussion Points", level=1)
        for point in mom.get("key_discussion_points", []):
            doc.add_heading(point.get("topic", "Topic"), level=2)
            doc.add_paragraph(point.get("summary", ""))

        # Decisions
        doc.add_heading("Decisions Made", level=1)
        for d in mom.get("decisions_made", []):
            doc.add_paragraph(d, style="List Bullet")

        # Action Items Table
        doc.add_heading("Action Items", level=1)
        table = doc.add_table(rows=1, cols=4)
        hdr_cells = table.rows[0].cells
        hdr_cells[0].text = 'Task'
        hdr_cells[1].text = 'Owner'
        hdr_cells[2].text = 'Priority'
        hdr_cells[3].text = 'Due Date'

        for item in mom.get("action_items", []):
            row_cells = table.add_row().cells
            row_cells[0].text = item.get("title", "")
            row_cells[1].text = item.get("assignee_email", "")
            row_cells[2].text = item.get("priority", "MEDIUM")
            row_cells[3].text = item.get("due_date", "")

        # Save to buffer
        file_stream = io.BytesIO()
        doc.save(file_stream)
        file_stream.seek(0)
        return file_stream
