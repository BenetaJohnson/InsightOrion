import io
import json
from docx import Document as DocxDocument
from typing import Dict

class ExportService:
    @staticmethod
    def to_markdown(mom: Dict) -> str:
        """Converts MoM JSON data into neat Markdown text."""
        md = []
        md.append(f"# Minutes of Meeting: {mom.get('title', 'Meeting Summary')}")
        md.append(f"**Date:** {mom.get('date', 'N/A')}  ")
        md.append(f"**Generated by:** InsightOrion  \n")
        
        md.append("## Executive Summary")
        md.append(f"{mom.get('executive_summary', 'No summary generated.')}\n")
        
        md.append("## Agenda Covered")
        for agenda in mom.get("agenda_covered", []):
            md.append(f"- {agenda}")
        md.append("")

        md.append("## Key Discussion Points")
        for point in mom.get("key_discussion_points", []):
            md.append(f"### {point.get('topic')}")
            md.append(f"{point.get('summary')}\n")
            
        md.append("## Decisions Made")
        for decision in mom.get("decisions_made", []):
            md.append(f"- {decision}")
        md.append("")

        md.append("## Action Items")
        md.append("| Task | Assignee | Priority | Due Date | Description |")
        md.append("| :--- | :--- | :--- | :--- | :--- |")
        for action in mom.get("action_items", []):
            md.append(f"| {action.get('title')} | {action.get('assignee_email')} | {action.get('priority')} | {action.get('due_date')} | {action.get('description')} |")
        md.append("")

        md.append("## Risks & Blocker Dependencies")
        for risk in mom.get("risks", []):
            md.append(f"- **Risk:** {risk.get('description')}")
            md.append(f"  - **Dependency:** {risk.get('dependency')}")
            md.append(f"  - **Escalation Plan:** {risk.get('escalation')}")
        md.append("")

        md.append("## Open Questions")
        for q in mom.get("open_questions", []):
            md.append(f"- {q}")
        md.append("")

        md.append("## Next Meeting Suggestions")
        sug = mom.get("next_meeting_suggestions", {})
        md.append(f"**Recommended Agenda:** {', '.join(sug.get('agenda', []))}")
        md.append(f"**Recommended Participants:** {', '.join(sug.get('recommended_participants', []))}")
        
        return "\n".join(md)

